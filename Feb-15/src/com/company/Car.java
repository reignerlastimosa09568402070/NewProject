package com.company;

public class Car {

	private int km;
	private String brand;
	private int velocity;
	
	
	
	

	public int getVelocity() {
		return velocity;
		
	}



	public void setVelocity(int velocity) {
		this.velocity = velocity;
	}



	public int getKm() {
		return km;
	}

	public void setKm(int km) {
		this.km = km;
	}

	public String getBrand() {
		return brand;
	}

	public void setBrand(String brand) {
		this.brand = brand;
	}
	
	
}
